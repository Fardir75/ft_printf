#include "ft_printf.h"

int	ft_count(int nb)
{
	int	count;

	count = 0;
	if (nb < 0)
		count++;
	if (nb == 0)
		return (1);
	while (nb != 0)
	{
		nb /= 10;
		count++;
	}
	return (count);
}
